public class Exercicio4 {
    public static void main(String[] args) {
        // Usando for
        System.out.println("Usando for:");
        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
        
        System.out.println("\n\nUsando while:");
        // Usando while
        int j = 1;
        while (j <= 100) {
            System.out.print(j + " ");
            j++;
        }
    }
}